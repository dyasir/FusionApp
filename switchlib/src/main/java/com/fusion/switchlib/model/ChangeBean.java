package com.fusion.switchlib.model;

public class ChangeBean {

    private Integer videoShowNumber;
    private Integer watchVideoLongtime;
    private Integer appStartNumber;
    private Boolean appChangeEnable;

    public Integer getVideoShowNumber() {
        return videoShowNumber;
    }

    public void setVideoShowNumber(Integer videoShowNumber) {
        this.videoShowNumber = videoShowNumber;
    }

    public Integer getWatchVideoLongtime() {
        return watchVideoLongtime;
    }

    public void setWatchVideoLongtime(Integer watchVideoLongtime) {
        this.watchVideoLongtime = watchVideoLongtime;
    }

    public Integer getAppStartNumber() {
        return appStartNumber;
    }

    public void setAppStartNumber(Integer appStartNumber) {
        this.appStartNumber = appStartNumber;
    }

    public Boolean getAppChangeEnable() {
        return appChangeEnable;
    }

    public void setAppChangeEnable(Boolean appChangeEnable) {
        this.appChangeEnable = appChangeEnable;
    }
}
